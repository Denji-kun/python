#string 
a = "hello"
print(type(a))
print (a)
# to make anything string use "" or ''.

#integer 
a = -15
b = 19 
print(type(a))
print(type(b))
# anything from -ve infinty to +ve infinty is integer.

#float
a = 19.2
b = 9/3
print(type(a))
print(type(b))
# any decimal or fraction (even if completely divisible) are float.

#boolean
a = True
b =  False
print(type(a))
print (type(b))
# true or false value is boolean.

#none 
a = None
print(type(a))
# any variable storing "None" value is None type, do not use "none" only "None" is allowed.

#complex
y = 19 + 3j
print(type(y))
# complex type is represented using 'j' even without any real number.
